function convertDigitToWord(inputValue) {
    var digitAsTxt = '';
    switch (inputValue) {
        case 1:
            digitAsTxt = '1';
            break;
        case 2:
            digitAsTxt = '2';
            break;
        case 3:
            digitAsTxt = '3';
            break;
        case 4:
            digitAsTxt = '4';
            break;
        case 5:
            digitAsTxt = '5';
            break;
        case 6:
            digitAsTxt = '6';
            break;
        case 7:
            digitAsTxt = '7';
            break;
        case 8:
            digitAsTxt = '8';
            break;
        case 9:
            digitAsTxt = '9';
            break;
    }
    return console.log(digitAsTxt);
}